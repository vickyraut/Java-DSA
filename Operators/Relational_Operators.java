public class Relational_Operators {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println((a==b));// It is == not =
        System.out.println((a!=b));
        System.out.println((a>b));
        System.out.println((a<b));
        System.out.println((a>=b));
        System.out.println((a<=b));
    }
}
