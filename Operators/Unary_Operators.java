public class Unary_Operators {
    public static void main(String[] args) {
        int a = 10;
        
        System.out.println("Post Imcrement = " + (a++)); // value get printed & get Incrimented to 11
        System.out.println("Pre Imcrement = " + (++a)); // value get get Incrimented to 12 & get printed
        System.out.println("Post Decrement = " + (a--)); // value get printed & get Decrimented to 11
        System.out.println("Pre Decrement = " + (--a)); // value get get Decrimented to 11 & get printed
    }
}
